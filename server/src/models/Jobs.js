const mongoose = require("mongoose");

const jobsSchema = new mongoose.Schema(
  {
    job: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    location: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
    versionKey: false,
  }
);

module.exports = mongoose.model("Jobs", jobsSchema);
